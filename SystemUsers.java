/**
 * Interface SystemUser. It has fundamental methods.
 *
 * @author Nilay Keven
 */
public interface SystemUsers
{
	/**
	 * Getter of the firstname that the person.
	 *
	 * @return String
	 */
	public String getFirstName();
	/**
	 * Getter of the secondname that the person.
	 *
	 * @return String
	 */
	public String getSecondName();
	/**
	 * Getter of the ID that the person.
	 *
	 * @return Integer
	 */
	public int getPersonID();
	/**
	 * Getter of the password that the person.
	 *
	 * @return String
	 */
	public String getPassword();

}